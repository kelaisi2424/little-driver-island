export const DRIVING_LEVELS: readonly DrivingLevel[] = drivingLevelSeeds.map((level, index) => ({
  ...level,
  id: index + 1,
  categoryName: CATEGORY_NAMES[level.category],
}));

export const LEVELS: readonly LevelDefinition[] = [
  ...DRIVING_LEVELS.slice(0, 5).map((level) => ({
    gameId: 'car-rush' as GameId,
    level: level.indexInCategory,
    goal: level.mission,
    difficulty: level.difficulty,
    learningGoal: level.learningGoal,
    summary: level.summary,
    config: { map: level.map },
  })),
  ...DRIVING_LEVELS.slice(5, 10).map((level) => ({
    gameId: 'parking-move' as GameId,
    level: level.indexInCategory,
    goal: level.mission,
    difficulty: level.difficulty,
    learningGoal: level.learningGoal,
    summary: level.summary,
    config: { map: level.map },
  })),
  ...DRIVING_LEVELS.slice(15, 20).map((level) => ({
    gameId: 'bus-pickup' as GameId,
    level: level.indexInCategory,
    goal: level.mission,
    difficulty: level.difficulty,
    learningGoal: level.learningGoal,
    summary: level.summary,
    config: { map: level.map },
  })),
];

export function getDrivingLevel(levelId: number): DrivingLevel {
  return DRIVING_LEVELS.find((level) => level.id === levelId) ?? DRIVING_LEVELS[0];
}

export function getDrivingLevelsByCategory(category: DrivingLevelType): DrivingLevel[] {
  return DRIVING_LEVELS.filter((level) => level.category === category);
}

export function getLevelsForGame(gameId: GameId): LevelDefinition[] {
  const levels = LEVELS.filter((level) => level.gameId === gameId);
  return levels.length ? levels : LEVELS.filter((level) => level.gameId === 'car-rush');
}

export function getLevel(gameId: GameId, levelNumber: number): LevelDefinition {
  return getLevelsForGame(gameId).find((level) => level.level === levelNumber)
    ?? getLevelsForGame(gameId)[0];
}
